package generated

import (
	"go/ast"
	"go/parser"
	"go/token"
	"testing"

	"golang.org/x/tools/go/analysis"
)

func TestFromPassFindsGeneratedFiles(t *testing.T) {
	fset := token.NewFileSet()
	generatedFile := parseFile(t, fset, "generated.go", "// Code generated by test. DO NOT EDIT.\npackage example\nfunc generated() {}")
	handwrittenFile := parseFile(t, fset, "handwritten.go", "package example\nfunc handwritten() {}")
	files := FromPass(&analysis.Pass{Fset: fset, Files: []*ast.File{generatedFile, handwrittenFile}})
	if !files.Contains(fset, generatedFile.Pos()) {
		t.Fatal("generated file was not detected")
	}
	if files.Contains(fset, handwrittenFile.Pos()) {
		t.Fatal("handwritten file was detected as generated")
	}
}

func parseFile(t *testing.T, fset *token.FileSet, name, source string) *ast.File {
	t.Helper()
	file, err := parser.ParseFile(fset, name, source, parser.ParseComments)
	if err != nil {
		t.Fatal(err)
	}
	return file
}
